                                    <h4> Quais os requisitos para entrar numa Startup? </h4>
                                    <p><br/></p>

                                    <div class="figura">
                                        <p class="text-center"><img src="../assets/images/Blog/startup-skills.jpeg"  alt="Gama Academy">
                                        <p align="center">Créditos: Outbound Marketing
                                    </div>
                                    <p><br/><br/></p>

                                    <p>Além de capacitação técnica é preciso ter <strong><a href="https://meuaio.com/blog/fit-cultural/" target="_blank">
                                                Skills Comportamentais</a></strong>. Afinal de cada 5
                                        demissões no Brasil, 3 são por causa de comportamento, e numa startup isso se 
                                        eleva na quinta potência, afinal não é todo mundo que tem o perfil para trabalhar em uma.</p>


                                    <p>Recomendo sempre olhar o que as empresas pedem nas vagas. <strong>Na RockJobs é muito fácil abrir 
                                        e ver o que o pessoal geralmente pede.</strong></p>


                                    <p>Exemplo (Alguns pontos interessantes da descrição de vaga de Inside Sales no BarbaRuiva, 
                                        <strong>corre que ainda tá aberta)</strong>:
                                        <br/>
                                        <strong><h5><a href="https://aio.recruitee.com/o/inside-sales-representative" target="_blank">
                                                <div class="text-center"> MeuAio</div>
                                            </a></strong></h5>
                                    </p>


                                    <p>
                                        <ol>
                                            <li>Desejam construir e fazer parte de algo grande;</li>
                                            <li>Trabalham duro e gostam de obter o justo reconhecimento por isso;</li>
                                            <li>São verdadeiramente curiosos e apaixonados por negócios e pessoas;</li>
                                            <li>Sabem trabalhar em equipe, interagir com outros times e que estão sempre dispostos a ajudar;</li>
                                            <li>Detestam pessoas que dão desculpas.</li>
                                            <li>Ser auto-gerenciável (em outras palavras um adulto responsável);</li>
                                            <li>Não ser doente por trabalho e dedicar tempo para sua família (ou pra quem você ama. 
                                                Espero que você ame a sua família);</li>
                                            <li>Não se importar de não ter um chefe e trabalhar de forma horizontal (você 
                                                já é grande o bastante para assumir responsabilidades e responder por elas);</li>
                                            <li>Aceitar que, cumprindo a sprint até quinta, você poderá folgar na sexta 
                                                (não, seu salário não será descontado);</li>
                                            <li>Torcer pelo BarbaRuiva.com mais que torce pela Apple/Google ou Cruzeiro/Atlético #EuAcredito;</li>
                                            <li>Fazer exercícios físicos no mínimo 3 vezes por semana num período mínimo de 30 minutos 
                                                (exercícios ajudam a ter foco, são anti-depressivos e ajudam na criatividade e disciplina);</li>
                                            <li>Comer alguma salada nas refeições diariamente (só milho e tomate não vale);</li>
                                            <li>Topar poder trabalhar de casa quando for necessário (as vezes precisa cuidar do filho, 
                                                da avó ou pra ficar mais na sua);</li>
                                            <li>Saber respeitar as pessoas em suas respectivas diversidades, não importando quais sejam 
                                                (futebol, sexo, fé, etc);</li>
                                            <li>Ter o costume de ler ao menos 1 livro por mês.</li>
                                        </ol>
                                    </p>


                                    <p>Esse é um exemplo de descrição de vaga que você vai encontrar aos montes. Claro que esse 
                                        é só um exemplo, mas no geral eles sempre pedem pessoas empreendedoras, auto-gerenciáveis, 
                                        responsáveis, inovadoras, interessadas e que possuam resiliência.</p>


